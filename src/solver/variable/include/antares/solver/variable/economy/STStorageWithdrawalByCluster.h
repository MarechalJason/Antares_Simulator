#pragma once

#include "STStorageByCluster_base.h"

namespace Antares::Solver::Variable::Economy
{
struct STStorageWithdrawalByClusterTraits
{
    static std::string Caption()
    {
        return "STS withdrawal by plant";
    }

    static std::string Unit()
    {
        return "P-withdrawal - MW";
    }

    static std::string Description()
    {
        return "Energy withdrawn by ST storage clusters";
    }

    static void computeStats(IntermediateValues& intermediateValues)
    {
        intermediateValues.computeStatisticsForTheCurrentYear();
    }

    static void setHourlyValue(std::vector<IntermediateValues>& clusterValues, State& state)
    {
        for (uint clusterIndex = 0; clusterIndex != state.area->shortTermStorage.count();
             ++clusterIndex)
        {
            clusterValues[clusterIndex].hour[state.hourInTheYear]
              = state.hourlyResults->ShortTermStorage[clusterIndex].withdrawal[state.hourInTheWeek];
        }
    }
};

using VCardSTstorageWithdrawalByCluster =
  VCardSTStorageByClusterBase<STStorageWithdrawalByClusterTraits>;

/*!
** \brief Energy generated by short term storage clusters
*/
template<class NextT = Container::EndOfList>
using STstorageWithdrawalByCluster = STStorageByClusterBase<STStorageWithdrawalByClusterTraits,
                                                            NextT>;

} // End namespace Antares::Solver::Variable::Economy
