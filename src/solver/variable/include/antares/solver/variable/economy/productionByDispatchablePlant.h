#pragma once

#include "DispatchablePlantByCluster_base.h"

namespace Antares::Solver::Variable::Economy
{
struct ProductionByDispatchablePlantTraits
{
    static std::string Caption()
    {
        return "DTG by plant";
    }

    static std::string Unit()
    {
        return "MWh";
    }

    static std::string Description()
    {
        return "Energy generated by all the clusters";
    }

    using AuxiliaryDataType = std::vector<std::vector<double>>;

    static void initializeAuxiliaryData(AuxiliaryDataType& pMinBySpace,
                                        Data::Study*,
                                        unsigned int nbYearsParallel,
                                        size_t nbClusters)
    {
        pMinBySpace.assign(nbYearsParallel, std::vector<double>(nbClusters * HOURS_PER_YEAR, 0.));
    }

    static void yearBegin(AuxiliaryDataType& pMinBySpace,
                          unsigned int,
                          unsigned int numSpace,
                          size_t)
    {
        std::fill(pMinBySpace[numSpace].begin(), pMinBySpace[numSpace].end(), 0.);
    }

    static void setHourlyValue(std::vector<IntermediateValues>& clusterValues,
                               AuxiliaryDataType& pMinBySpace,
                               State& state,
                               unsigned int numSpace)
    {
        auto& area = state.area;
        auto& thermal = state.thermal;
        for (auto& cluster: area->thermal.list.each_enabled())
        {
            clusterValues[cluster->enabledIndex].hour[state.hourInTheYear]
              += thermal[area->index].thermalClustersProductions[cluster->enabledIndex];

            pMinBySpace[numSpace][(cluster->enabledIndex * HOURS_PER_YEAR) + state.hourInTheYear]
              = thermal[area->index].PMinOfClusters[cluster->enabledIndex];
        }
    }

    static void yearEndBuildPrepareDataForEachThermalCluster(
      std::vector<std::vector<IntermediateValues>>& yearlyValues,
      AuxiliaryDataType& pMinBySpace,
      State& state,
      uint,
      unsigned int numSpace)
    {
        for (unsigned int i = 0; i <= state.study.runtime.rangeLimits.hour[Data::rangeEnd]; ++i)
        {
            state.thermalClusterProductionForYear[i] += yearlyValues[numSpace][state.thermalCluster
                                                                                 ->enabledIndex]
                                                          .hour[i];
            state.thermalClusterPMinOfTheClusterForYear[i] += pMinBySpace
              [numSpace][(state.thermalCluster->enabledIndex * HOURS_PER_YEAR) + i];
        }
    }
};

using VCardProductionByDispatchablePlant = VCardDispatchablePlantByClusterBase<
  ProductionByDispatchablePlantTraits>;

/*!
** \brief Energy generated by all thermal dispatchable clusters
*/
template<class NextT = Container::EndOfList>
using ProductionByDispatchablePlant = DispatchablePlantByClusterBase<
  ProductionByDispatchablePlantTraits>;

} // namespace Antares::Solver::Variable::Economy
