#pragma once

#include "STStorageByCluster_base.h"

namespace Antares::Solver::Variable::Economy
{
struct ProductionByRenewablePlantTraits
{
    static std::string Caption()
    {
        return "RES generation by plant";
    }

    static std::string Unit()
    {
        return "MWh";
    }

    static std::string Description()
    {
        return "Energy generated by all the RES clusters";
    }

    static constexpr uint8_t fileLevel = Category::FileLevel::de_res;

    static size_t clusterCount(const Data::Area* area)
    {
        return area->renewable.list.enabledCount();
    }

    static void computeStats(IntermediateValues& iv)
    {
        iv.computeStatisticsForTheCurrentYear();
    }

    static void setHourlyValue(std::vector<IntermediateValues>& clusterValues, State& state)
    {
        for (const auto& renewableCluster: state.area->renewable.list.each_enabled())
        {
            double renewableClusterProduction = renewableCluster->valueAtTimeStep(state.year,
                                                                                  state.hourInTheYear);

            clusterValues[renewableCluster->enabledIndex].hour[state.hourInTheYear]
              += renewableClusterProduction;
        }
    }

    static void buildSurveyReport(const std::vector<IntermediateValues>& clusterValues,
                                  SurveyResults& results,
                                  int fileLevel,
                                  int precision)
    {
        const auto& renewable = results.data.area->renewable;
        for (const auto& cluster: renewable.list.each_enabled())
        {
            results.variableCaption = cluster->name();
            results.variableUnit = Unit();
            clusterValues[cluster->enabledIndex].template buildAnnualSurveyReport<
              VCardSTStorageByClusterBase<ProductionByRenewablePlantTraits>>(results, fileLevel, precision);
        }
    }
};

using VCardProductionByRenewablePlant = VCardSTStorageByClusterBase<ProductionByRenewablePlantTraits>;

/*!
** \brief Energy generated by all renewable clusters
*/
template<class NextT = Container::EndOfList>
using ProductionByRenewablePlant = STStorageByClusterBase<ProductionByRenewablePlantTraits, NextT>;

} // namespace Antares::Solver::Variable::Economy

