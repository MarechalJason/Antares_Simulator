import csv
import io
from typing import Optional, Tuple, List
from pathlib import Path


def parse_csv_lines(lines):
    """
    Parse lines from a CSV file into a list of dictionaries.
    Each dictionary represents a row, with column headers as keys.
    """
    if lines is None:
        return None
    reader = csv.DictReader(io.StringIO("".join(lines)))
    return list(reader)


def key_of_row(row: dict):
    """
    Generate a stable key for a given row to uniquely identify it.
    It first tries to use a predefined set of columns. If they are not all
    present, it falls back to using all columns sorted by name.
    """
    # Stable key for identifying a row
    keys = [
        "block",
        "component",
        "output",
        "absolute_time_index",
        "block_time_index",
        "scenario_index",
    ]
    if all(k in row for k in keys):
        return tuple((row[k] or "").strip() for k in keys)
    # Fallback: all values ordered by column name
    colnames = sorted(k for k in row.keys() if k is not None)
    return ("__all__", tuple((k, (row[k] or "").strip()) for k in colnames))
    """
    Example: output missing, fallback to all columns
    (
        '__all__',
        (
            ('absolute_time_index', '124'),
            ('block', 'Mon Bloc 2'),
            ('block_time_index', '46'),
            ('colonne_speciale', 'valeur Z'),
            ('component', 'Ma Comp. 2'),
            ('scenario_index', '2')
        )
    )
    """


def index_rows(rows):
    """
    Create an index of rows from a list of rows.
    The index is a dictionary where keys are generated by `key_of_row`
    and values are lists of rows matching that key.
    """
    index = {}
    for r in rows:
        index.setdefault(key_of_row(r), []).append(r)
    return index


def as_number(s: str):
    """
    Try to convert a string to a float.
    Returns the float on success, None on failure.
    """
    try:
        return float(s)
    except Exception:
        return None


def compare_rows(r_ref: dict, r_out: dict):
    """
    Compare two rows (as dictionaries) and report differences.
    It compares values of common columns and also reports columns that are
    present in the reference row but missing from the output row.
    A numeric comparison is performed for values that can be converted to floats.
    """
    diffs = {}
    # Compare common columns; also report missing columns
    ref_cols = {k for k in r_ref.keys() if k}
    out_cols = {k for k in r_out.keys() if k}
    common = ref_cols & out_cols
    if "basis_status" in common:
        common.remove("basis_status")

    for c in sorted(common):
        a_raw = r_ref[c] if r_ref[c] is not None else ""
        b_raw = r_out[c] if r_out[c] is not None else ""
        a = a_raw.strip()
        b = b_raw.strip()
        if a == b:
            continue
        a_num = as_number(a)
        b_num = as_number(b)
        if a_num is not None and b_num is not None:
            # Numeric compare to avoid false positives like '1' vs '1.0'
            if a_num != b_num:
                diffs[c] = (a_raw, b_raw)
        else:
            diffs[c] = (a_raw, b_raw)
    missing_in_out = sorted(ref_cols - out_cols)
    if missing_in_out:
        diffs["__missing_in_out__"] = missing_in_out
    return diffs


def format_key(key_tuple):
    """
    Format a key tuple into a human-readable string for display in messages.
    """
    # Format a readable key
    labels = [
        "block",
        "component",
        "output",
        "absolute_time_index",
        "block_time_index",
        "scenario_index",
    ]
    if key_tuple and key_tuple[0] == "__all__":
        return "(raw-row-key from all columns)"
    parts = []
    for i, v in enumerate(key_tuple):
        if i < len(labels):
            parts.append(f"{labels[i]}={v}")
        else:
            parts.append(str(v))
    return ", ".join(parts)

def compare_folders(folder1: Path, folder2: Path) -> Tuple[bool, List[str]]:
    """
    Compare files between two folders.

    Args:
        folder1: First folder (reference)
        folder2: Second folder (to compare against)

    Returns:
        Tuple of (all_match: bool, differences: List[str])
        - all_match: True if all files exist and have identical content
        - differences: List of difference descriptions
    """
    differences = []

    if not folder1.exists():
        differences.append(f"Folder 1 does not exist: {folder1}")
        return False, differences

    if not folder2.exists():
        differences.append(f"Folder 2 does not exist: {folder2}")
        return False, differences

    # Get all files in folder1 recursively
    folder1_files = [f for f in folder1.rglob('*') if f.is_file()]

    if not folder1_files:
        differences.append(f"No files found in folder 1: {folder1}")
        return False, differences

    for file1 in folder1_files:
        # Get relative path from folder1
        rel_path = file1.relative_to(folder1)
        file2 = folder2 / rel_path

        # Check if file exists in folder2
        if not file2.exists():
            differences.append(f"Missing file in folder 2: {rel_path}")
            continue

        # Compare file contents
        try:
            content1 = file1.read_text()
            content2 = file2.read_text()

            if content1 != content2:
                differences.append(f"Content mismatch: {rel_path}")
        except Exception as e:
            differences.append(f"Error reading {rel_path}: {str(e)}")

    return len(differences) == 0, differences
